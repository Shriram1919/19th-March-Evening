package loops;

public class Forloop {

	public static void main(String[] args) {
		
		
		for(int i=1;i<=10;i++)
		{
		System.out.println("Shriram");	
		}
			
		
		for(int a=2;a<=20;a=a+2) 
		{
			System.out.println(a);
			
		}
		
		
		for(int b=80;b>=8;b=b-8)
		{
			System.out.println(b);
		
		}
		
		
		
		
		
		
		
		
		
		
	    }
}


