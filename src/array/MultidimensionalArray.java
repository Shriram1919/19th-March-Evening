package array;

public class MultidimensionalArray {

	public static void main(String[] args) {
		
		
		
		char grade [][]= {{'A','B','E'},{'C','D','G'}};
	
		for(int i=0;i<=1;i++) 
		{
			for(int j=0;j<=2;j++)
			{
				System.out.print(grade[i][j]+" ");
			}
			System.out.println();
		}
		
		
	}

}
